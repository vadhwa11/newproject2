package jkt.hms.medicalboard.controller;

public class CertificateByTheCandidateSearchDTO {

	private String entryNo;
	private String entryDate;
	private String batchNo;
	private String chestNo;
	private String name;
	private String place;
	private String appealExamination;

	/**
	 * @return the entryNo
	 */
	public String getEntryNo() {
		return entryNo;
	}

	/**
	 * @param entryNo
	 *            the entryNo to set
	 */
	public void setEntryNo(String entryNo) {
		this.entryNo = entryNo;
	}

	/**
	 * @return the entryDate
	 */
	public String getEntryDate() {
		return entryDate;
	}

	/**
	 * @param entryDate
	 *            the entryDate to set
	 */
	public void setEntryDate(String entryDate) {
		this.entryDate = entryDate;
	}

	/**
	 * @return the batchNo
	 */
	public String getBatchNo() {
		return batchNo;
	}

	/**
	 * @param batchNo
	 *            the batchNo to set
	 */
	public void setBatchNo(String batchNo) {
		this.batchNo = batchNo;
	}

	/**
	 * @return the chestNo
	 */
	public String getChestNo() {
		return chestNo;
	}

	/**
	 * @param chestNo
	 *            the chestNo to set
	 */
	public void setChestNo(String chestNo) {
		this.chestNo = chestNo;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the place
	 */
	public String getPlace() {
		return place;
	}

	/**
	 * @param place
	 *            the place to set
	 */
	public void setPlace(String place) {
		this.place = place;
	}

	/**
	 * @return the appealExamination
	 */
	public String getAppealExamination() {
		return appealExamination;
	}

	/**
	 * @param appealExamination
	 *            the appealExamination to set
	 */
	public void setAppealExamination(String appealExamination) {
		this.appealExamination = appealExamination;
	}

}
