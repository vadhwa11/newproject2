package jkt.hms.pacs.controller;

import java.util.Date;

public class PacsPatient {

	private String patientId;
	private String patientName;
	private String gender;
	private String webViewer;
	private String img; 
	private Date   createDate;
	private String history; 
	private String reports; 
	private String reportstatus; 
	private String investigation;
	private String modality;
	private String studyurl;
	private String reportedBy;
	private String message;
	
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getStudyurl() {
		return studyurl;
	}
	public void setStudyurl(String studyurl) {
		this.studyurl = studyurl;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public String getPatientId() {
		return patientId;
	}
	public void setPatientId(String patientId) {
		this.patientId = patientId;
	}
	public String getPatientName() {
		return patientName;
	}
	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getWebViewer() {
		return webViewer;
	}
	public void setWebViewer(String webViewer) {
		this.webViewer = webViewer;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getHistory() {
		return history;
	}
	public void setHistory(String history) {
		this.history = history;
	}
	public String getReports() {
		return reports;
	}
	public void setReports(String reports) {
		this.reports = reports;
	}
	public String getReportstatus() {
		return reportstatus;
	}
	public void setReportstatus(String reportstatus) {
		this.reportstatus = reportstatus;
	}
	public String getInvestigation() {
		return investigation;
	}
	public void setInvestigation(String investigation) {
		this.investigation = investigation;
	}
	public String getModality() {
		return modality;
	}
	public void setModality(String modality) {
		this.modality = modality;
	}
	public String getReportedBy() {
		return reportedBy;
	}
	public void setReportedBy(String reportedBy) {
		this.reportedBy = reportedBy;
	}
	 
	
}
